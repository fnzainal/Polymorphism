package belajar;

public class PrintData {
	//method Print() dengan parameter String
	public void Print(String Cetak){
		System.out.println(Cetak);
	}
	//method Print() dengan parameter int
	public void Print(int Cetak){
		System.out.println(Cetak);
	}
	//method Print() dengan parameter double
	public void Print(double Cetak){
		System.out.println(Cetak);
	}
	//method Print() dengan parameter char
	public void Print(char Cetak){
		System.out.println(Cetak);
	}
}
